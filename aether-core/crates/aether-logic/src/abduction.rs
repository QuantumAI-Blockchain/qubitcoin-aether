#[cfg(test)]
mod tests {
    use super::*;
    use crate::term::{Symbol, Term};

    fn sym(id: u32) -> Symbol {
        Symbol(id)
    }

    fn medical_kb() -> KnowledgeBase {
        let flu = sym(0);
        let fever = sym(1);
        let cough = sym(2);
        let cold = sym(3);
        let infection = sym(4);

        let mut kb = KnowledgeBase::new();

        // Facts: patient has fever and cough
        kb.add_fact(Formula::atom(fever, vec![Term::Constant(sym(10))])); // fever(patient)
        kb.add_fact(Formula::atom(cough, vec![Term::Constant(sym(10))])); // cough(patient)

        // Rules:
        // flu(X) -> fever(X)
        kb.add_rule(Formula::implies(
            Formula::atom(flu, vec![Term::var(0)]),
            Formula::atom(fever, vec![Term::var(0)]),
        ));
        // cold(X) -> cough(X)
        kb.add_rule(Formula::implies(
            Formula::atom(cold, vec![Term::var(0)]),
            Formula::atom(cough, vec![Term::var(0)]),
        ));
        // infection(X) -> fever(X)
        kb.add_rule(Formula::implies(
            Formula::atom(infection, vec![Term::var(0)]),
            Formula::atom(fever, vec![Term::var(0)]),
        ));
        // flu(X) -> cough(X)  (flu also explains cough)
        kb.add_rule(Formula::implies(
            Formula::atom(flu, vec![Term::var(0)]),
            Formula::atom(cough, vec![Term::var(0)]),
        ));

        kb
    }

    #[test]
    fn test_abduce_finds_hypotheses() {
        let kb = medical_kb();
        let fever = sym(1);
        let observation = Formula::atom(fever, vec![Term::Constant(sym(10))]);

        let hypotheses = abduce(&kb, &observation);
        assert!(
            !hypotheses.is_empty(),
            "Should find at least one hypothesis for fever"
        );

        // Should find flu(patient) and infection(patient) as hypotheses
        assert!(
            hypotheses.len() >= 2,
            "Expected at least 2 hypotheses, got {}",
            hypotheses.len()
        );
    }

    #[test]
    fn test_abduce_ranking() {
        let kb = medical_kb();
        let fever = sym(1);
        let observation = Formula::atom(fever, vec![Term::Constant(sym(10))]);

        let hypotheses = abduce(&kb, &observation);
        // Scores should be in descending order.
        for i in 1..hypotheses.len() {
            assert!(
                hypotheses[i - 1].score >= hypotheses[i].score,
                "Hypotheses not sorted by score"
            );
        }
    }

    #[test]
    fn test_abduce_explanatory_power() {
        let kb = medical_kb();
        let cough = sym(2);
        let observation = Formula::atom(cough, vec![Term::Constant(sym(10))]);

        let hypotheses = abduce(&kb, &observation);
        // flu(patient) should have higher explanatory power than cold(patient)
        // because flu also explains fever (which is a known fact).
        // Find flu hypothesis
        let flu = sym(0);
        let flu_hyp = hypotheses.iter().find(|h| {
            matches!(&h.formula, Formula::Atom(p, _) if *p == flu)
        });
        assert!(
            flu_hyp.is_some(),
            "Should find flu as hypothesis for cough"
        );
    }

    #[test]
    fn test_abduce_no_hypotheses_for_unknown() {
        let kb = medical_kb();
        // Query something with no matching rule consequent.
        let unknown = sym(99);
        let observation = Formula::atom(unknown, vec![Term::Constant(sym(10))]);
        let hypotheses = abduce(&kb, &observation);
        assert!(hypotheses.is_empty());
    }

    #[test]
    fn test_abduce_consistency_filters_contradictions() {
        let p = sym(0);
        let q = sym(1);
        let patient = sym(10);

        let mut kb = KnowledgeBase::new();
        kb.add_fact(Formula::atom(q, vec![Term::Constant(patient)]));
        // ~p(X) is a known fact
        kb.add_fact(Formula::not(Formula::atom(p, vec![Term::Constant(patient)])));
        // Rule: p(X) -> q(X)
        kb.add_rule(Formula::implies(
            Formula::atom(p, vec![Term::var(0)]),
            Formula::atom(q, vec![Term::var(0)]),
        ));

        let observation = Formula::atom(q, vec![Term::Constant(patient)]);
        let hypotheses = abduce(&kb, &observation);
        // p(patient) contradicts ~p(patient), so it should score 0 on consistency.
        for h in &hypotheses {
            if matches!(&h.formula, Formula::Atom(pred, _) if *pred == p) {
                assert!(
                    h.score < 0.5,
                    "Contradictory hypothesis should have low score, got {}",
                    h.score
                );
            }
        }
    }

    #[test]
    fn test_simplicity_prefers_simpler() {
        // A simpler hypothesis (fewer terms) should score higher on simplicity.
        let simple = Formula::atom(sym(0), vec![Term::Constant(sym(10))]);
        let complex = Formula::and(vec![
            Formula::atom(sym(0), vec![Term::Constant(sym(10))]),
            Formula::atom(sym(1), vec![Term::Constant(sym(10)), Term::Constant(sym(11))]),
        ]);
        let s1 = simplicity_score(&simple);
        let s2 = simplicity_score(&complex);
        assert!(s1 > s2, "Simpler formula should have higher simplicity score");
    }
}
